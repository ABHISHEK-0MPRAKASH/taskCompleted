package com.java.collections.customcollections;

public class Student {
    String name;
    String usn;
    int englishMarks;
    int mathsMarks;
    int chemistryMarks;
    int biologyMarks;

    public Student(String name, String usn, int englishMarks, int mathsMarks, int chemistryMarks, int biologyMarks) {
        this.name = name;
        this.usn = usn;
        this.englishMarks = englishMarks;
        this.mathsMarks = mathsMarks;
        this.chemistryMarks = chemistryMarks;
        this.biologyMarks = biologyMarks;
    }
}
