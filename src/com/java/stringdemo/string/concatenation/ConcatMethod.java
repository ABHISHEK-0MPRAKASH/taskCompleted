public class ConcatMethod {
    public static void main(String[] args) {
        String s1 = "Virat ";
        String s2 = "Kohli";
        String s3 = s1.concat(s2);

        System.out.println(s1);
        System.out.println(s2);
        System.out.println(s3);
    }
}
